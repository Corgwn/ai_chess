use value_tables::*;

pub(crate) fn piece_square_value(
    piece_type: PieceTypes,
    piece_color: PieceColors,
    pos: Position,
    endgame: bool,
) -> i32 {
    if piece_color == PieceColors::Black {
        match piece_type {
            PieceTypes::Knight => BLACK_KNIGHT_TABLE[pos.value],
            PieceTypes::Rook => BLACK_ROOK_TABLE[pos.value],
            PieceTypes::Bishop => BLACK_BISHOP_TABLE[pos.value],
            PieceTypes::Queen => BLACK_QUEEN_TABLE[pos.value],
            PieceTypes::King if endgame => BLACK_KING_EG_TABLE[pos.value],
            PieceTypes::King if !endgame => BLACK_KING_MG_TABLE[pos.value],
            PieceTypes::Pawn => BLACK_PAWN_TABLE[pos.value],
            _ => 0,
        }
    } else {
        match piece_type {
            PieceTypes::Knight => WHITE_KNIGHT_TABLE[pos.value],
            PieceTypes::Rook => WHITE_ROOK_TABLE[pos.value],
            PieceTypes::Bishop => WHITE_BISHOP_TABLE[pos.value],
            PieceTypes::Queen => WHITE_QUEEN_TABLE[pos.value],
            PieceTypes::King if endgame => WHITE_KING_EG_TABLE[pos.value],
            PieceTypes::King if !endgame => WHITE_KING_MG_TABLE[pos.value],
            PieceTypes::Pawn => WHITE_PAWN_TABLE[pos.value],
            _ => 0,
        }
    }
}
